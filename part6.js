// Write a for loop that prints numbers from 1 to 5.
for (let i = 1; i <= 5; i++) {
  console.log(i);
}
//  Write a loop that prints only even numbers from 1 to 10.
for (let i = 1; i <= 10; i++) {
  if (i % 2 == 0) {
    console.log(i);
  }
}
